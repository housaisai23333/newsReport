public class Colums {
    private int colums_id;
    private String colums_name;
    //无参构造方法

    public Colums() {
    }
    //get和set方法

    public int getColums_id() {
        return colums_id;
    }

    public void setColums_id(int colums_id) {
        this.colums_id = colums_id;
    }

    public String getColums_name() {
        return colums_name;
    }

    public void setColums_name(String colums_name) {
        this.colums_name = colums_name;
    }
}
